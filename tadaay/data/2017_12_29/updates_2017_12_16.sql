CREATE TABLE `app_polyglot_news` (
  `id` int(11) NOT NULL,
  `title` varchar(45) DEFAULT NULL,
  `organizer` varchar(45) DEFAULT NULL,
  `description` varchar(45) DEFAULT NULL,
  `recurring` varchar(45) DEFAULT NULL,
  `image_path` varchar(45) DEFAULT NULL,
  `venue` varchar(45) DEFAULT NULL,
  `location` varchar(45) DEFAULT NULL,
  `city` int(5) DEFAULT NULL,
  `country` int(5) DEFAULT NULL,
  `template` int(5) DEFAULT NULL,
  `meta_title` varchar(64) DEFAULT NULL,
  `meta_description` varchar(45) DEFAULT NULL,
  `meta_keywords` varchar(45) DEFAULT NULL,
  `is_active` varchar(45) DEFAULT NULL,
  `created_datetime` datetime DEFAULT NULL,
  `modified_datetime` datetime DEFAULT NULL,
  `last_modified_user_id` int(5) DEFAULT NULL,
  `event_identifier` varchar(45) DEFAULT NULL,
  `schedule` datetime DEFAULT NULL,
  PRIMARY KEY (`id`),
  KEY `city` (`city`),
  KEY `country` (`country`),
  KEY `template` (`template`),
  CONSTRAINT `app_polyglot_news_ibfk_1` FOREIGN KEY (`city`) REFERENCES `dmn_cities` (`city_id`),
  CONSTRAINT `app_polyglot_news_ibfk_2` FOREIGN KEY (`country`) REFERENCES `dmn_cntry` (`cntry_id`),
  CONSTRAINT `app_polyglot_news_ibfk_3` FOREIGN KEY (`template`) REFERENCES `app_polyglot_templates` (`template_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;